       try{
        TaskScheduler taskScheduler = (TaskScheduler)Naming.lookup("rmi://localhost:2077/TaskScheduler1");

        float a, b;
        INumberMultiplicationTask nm = (INumberMultiplicationTask)Naming.lookup("rmi://localhost:2077/TaskScheduler");
        a = Float.parseFloat(jTextField1.getText());
        b = Float.parseFloat(jTextField2.getText());
        INumberMultiplicationTask nm1 = nm.MultiplyNumber(a,b);
        taskScheduler.submitTask(nm1);
        nm1.execute();
        Float result2 = (Float)taskScheduler.getResult(nm1);

        // Update the result label
        jLabel3.setText(result2+"");
        }catch(Exception e){
            System.err.println("Error"+e);
        }
